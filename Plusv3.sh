check_compatibility() {
    print_progress "Checking System Compatibility"
    refresh_rate=$(dumpsys SurfaceFlinger | grep "refresh-rate" | awk '{print $3}' | head -1)
    [ -z "$refresh_rate" ] && refresh_rate=60
    vulkan_support=$(getprop ro.hardware.vulkan)
    [ -z "$vulkan_support" ] && vulkan_support="unknown"
    echo "[*] Refresh Rate: ${refresh_rate}Hz | Vulkan: $vulkan_support" >> "$log_file"
    echo "[+] Compatibility Check Passed"
}

